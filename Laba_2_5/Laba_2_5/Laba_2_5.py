import math

# Функция для вычисления площади равностороннего треугольника
def triangle_area(a):
    return (math.sqrt(3) / 4) * a * a

# Функция для вычисления площади правильного шестиугольника
def hexagon_area(a):
    return 6 * triangle_area(a)

# Ввод стороны шестиугольника
side = float(input("Введите длину стороны шестиугольника: "))

# Вывод площади
print(f"Площадь правильного шестиугольника со стороной {side} равна {hexagon_area(side):.2f}")

